<a name="op4" id="op4"></a>
<h2>4. Segnala errori e inesattezze</h2>
openpolis gestisce una quantit&agrave; enorme di informazioni (circa 130.000 politici) che sono soggette a continui cambiamenti (elezioni, dimissioni e sostituzioni degli incarichi, etc.) e che richiedono controlli e aggiornamenti continui.<br />
Le tue segnalazioni di errori, inesattezze e mancanze sono quindi preziose per mantenere sotto controllo l'aggiornamento e l'affidabilit&agrave; dei dati.<br />
Nelle pagine delle amministrazioni locali e in quelle dedicate al singolo politico trovi i tasti per inviare le segnalazioni con le informazioni necessarie alle modifiche.<br />
Utilizza gli stessi pulsanti per segnalare eventuali contenuti offensivi, inesatti, privi di fonte o che siano comunque in contrasto con il <?php echo link_to('regolamento', 'static/regolamento') ?> e le <?php echo link_to('condizioni d\'uso','static/condizioni') ?> del sito. 

