<h2>2. Diventa socio/a dell'associazione openpolis </h2>
Chiediamo innanzitutto a voi utenti e visitatori dei nostri siti di diventare soci per contribuire a tenere in vita e a far crescere un'esperienza che è riconosciuta come unica in Italia e all'estero.<br/>
Per i soci sono previste tre fasce di quote:
<br/>
1. contributo annuale ordinario minimo 50 euro<br/>
2. contributo annuale di sostegno minimo 500 euro<br/>
3. contributo annuale di studenti minimo 20 euro<br/>
<br/>
Va da sé che ogni centesimo in entrata e in uscita verrà rendicontato con scrupolo. Per ogni attività ci sarà la massima trasparenza e possibilità di partecipare direttamente. Sono in preparazione pagine apposite dedicate ai soci, alla rendicontazione di attività e bilanci.
<br/>
Ai soci, oltre ai consueti diritti di partecipazione alla vita dell'associazione, vengono riservati i servizi <a href="http://parlamento.openpolis.it/sottoscrizioni_pro">premium di openparlamento</a>.
<br/>

<br/>
Per fare richiesta di adesione è sufficiente inviare una email a info@openpolis.it con le seguenti informazioni:<br/>

- Nome Cognome<br/>
- tipo di contributo (1,2,3) e importo<br/>
- Breve profilo personale con indicazione delle motivazioni e del tipo di contributi possibili alla vita dell'Associazione e ai suoi progetti<br/>
- Indirizzo e recapiti<br/>
