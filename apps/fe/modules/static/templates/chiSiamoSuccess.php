<h1 style="font-size:17px;">STATUTO DELL&#8217;ASSOCIAZIONE OPENPOLIS</h1>

<p>(Costituita a Roma il 24 luglio 2008)</p>

<h2 id="articolo_1_denominazione_sede">Articolo 1 (Denominazione – Sede)</h2>

<p>È costituita un&#8217;associazione indipendente, apartitica, aconfessionale e senza fini di lucro, denominata &#8220;OPENPOLIS&#8221;, con sede in Roma, Via Luigi Montuori n.5.</p>

<p>Con delibera del Consiglio Direttivo l&#8217;Associazione potrà istituire centri o sedi secondarie in qualsiasi parte d&#8217;Italia ed all&#8217;estero.</p>

<h2 id="articolo_2_durata">Articolo 2 (Durata)</h2>

<p>La durata dell&#8217;associazione è a tempo indeterminato.</p>

<h2 id="articolo_3_scopo">Articolo 3 (Scopo)</h2>

<p>L&#8217;Associazione intende usare le tecnologie e la rete per favorire la trasparenza pubblica e la partecipazione delle persone al controllo delle scelte di interesse collettivo.</p>

<p>L&#8217;Associazione si propone di:</p>

<ol>
<li>promuovere il diritto di accesso ai dati e alle informazioni di interesse collettivo perché siano effettivamente pubblici, a disposizione di chiunque senza limitazioni;</li>
<li>favorire la partecipazione diretta e l’intervento di persone e gruppi alle decisioni pubbliche;</li>
<li>favorire la comunicazione e le relazioni tra rappresentanti nelle istituzioni e i cittadini;</li>
<li>promuovere l’innovazione delle amministrazioni pubbliche in particolare attraverso l&#8217;adozione delle tecnologie della comunicazione e dell’informazione;</li>
<li>usare e diffondere il software libero e aperto;</li>
<li>diffondere la cultura e le pratiche dell&#8217;apertura (open source, open content, open publishing, etc.) e dei beni comuni;</li>
<li>rispettare e promuovere la riservatezza dei dati e delle informazioni personali;</li>
<li>collaborare con tutti i soggetti che in Italia e all&#8217;estero svolgono attività di ricerca e sperimentazione nei settori di intervento dell&#8217;Associazione.
L&#8217;Associazione potrà svolgere inoltre, sia attraverso la rete che in maniera tradizionale, attività di informazione, documentazione, comunicazione, editoria, ricerca, consulenza, formazione, organizzazione di eventi e ogni altra attività utile a perseguire gli scopi sociali.</li>
</ol>

<p>Per il raggiungimento delle proprie finalità L&#8217;Associazione può aderire a federazioni, enti ed organismi aventi scopi analoghi a quelli statutari, esistenti o da costituire.</p>

<h2 id="articolo_4_i_soci">Articolo 4 (I soci)</h2>

<p>Possono essere soci dell’associazione tutti coloro – persone fisiche e giuridiche – che condividono gli scopi dell’associazione e che condividono lo presente statuto e il regolamento interno.
I soci sono fondatori e ordinari.</p>

<p>Sono soci fondatori coloro che hanno costituito l&#8217;associazione e sono firmatari dell&#8217;atto costitutivo.
Sono soci ordinari coloro che vengono ammessi all&#8217;associazione previa richiesta di iscrizione.</p>

<p>Tutti i soci sono tenuti:</p>

<ol>
<li>al pagamento delle quote associative, nella misura e con la periodicità determinate dal Consiglio Direttivo;</li>
<li>al pagamento dei contributi straordinari eventualmente deliberati dal Consiglio Direttivo;</li>
<li>all&#8217;osservanza delle norme statutarie e del regolamento interno;</li>
<li>a contribuire lealmente con tutte le loro capacità al raggiungimento dei fini sociali.
I soci fondatori ed i soci ordinari hanno diritto a partecipare all&#8217;assemblea potendo esercitare il diritto di voto.</li>
</ol>

<p>I soci fondatori fanno parte di diritto del Consiglio Direttivo.</p>

<h2 id="articolo_5_ammissione_dei_soci">Articolo 5 (Ammissione dei soci)</h2>

<p>Le domande di ammissione sono approvate dal Presidente e ratificate dal Consiglio Direttivo.</p>

<p>I soggetti che propongono domanda di ammissione riconoscono automaticamente e contestualmente senza riserve il presente statuto e il regolamento interno.</p>

<p>Le somme versate per le quote sociali e per gli eventuali contributi stabiliti dall&#8217;associazione non sono rimborsabili in nessun caso.</p>

<p>La quota di ingresso verrà stabilita dal Consiglio Direttivo.
La quota sociale è intrasmissibile e non è rivalutabile.</p>

<h2 id="articolo_6_decadenza_dei_soci">Articolo 6 (Decadenza dei soci)</h2>

<p>Decadono dalla qualifica di soci coloro che:</p>

<ol>
<li>non siano in regola con il versamento della quota associativa;</li>
<li>con il loro comportamento ledano il buon nome dell&#8217;associazione;</li>
<li>commettano violazioni o inadempienze statutarie o al regolamento interno;</li>
<li>si dimettano;
dette decadenze, tranne le dimissioni, vengono decise dal Consiglio Direttivo.</li>
</ol>

<p>Le dimissioni vengono accettate dal Presidente e ratificate dal Consiglio Direttivo.</p>

<p>La perdita di qualità di socio comporta automaticamente decadenza dalle eventuali cariche sociali ricoperte.</p>

<h2 id="articolo_7_organi_sociali">Articolo 7 (Organi sociali)</h2>

<p>Sono organi dell’associazione:</p>

<ol>
<li>l&#8217;Assemblea dei soci;</li>
<li>il Consiglio Direttivo;</li>
<li>il Presidente;</li>
<li>il Segretario Amministrativo.</li>
</ol>

<h2 id="articolo_8_l8217assemblea_dei_soci">Articolo 8 (L&#8217;assemblea dei soci)</h2>

<p>L&#8217;assemblea generale è costituita da tutti i soci ordinari e fondatori.</p>

<p>Ogni socio ha diritto ad un voto.
L&#8217;assemblea è convocata di norma una volta l&#8217;anno od ogniqualvolta che il consiglio direttivo lo ritenga opportuno o su richiesta motivata di almeno un terzo degli associati.</p>

<p>L&#8217;assemblea è convocata mediante fax o mediante e-mail da inviarsi ai soci almeno dieci giorni prima di quello stabilito per l&#8217;adunanza, con l’indicazione dell&#8217;ordine del giorno.</p>

<p>L&#8217;assemblea sia ordinaria sia straordinaria è regolarmente costituita qualunque sia il numero dei soci intervenuti, ed ha facoltà di deliberare su tutte le questioni all&#8217;ordine del giorno, a maggioranza dei presenti.</p>

<p>Le funzioni dell&#8217;assemblea sono:</p>

<ol>
<li>approvare il bilancio consuntivo e quello preventivo predisposti dal Consiglio Direttivo;</li>
<li>eleggere i componenti del Consiglio Direttivo ad eccezione del primo Consiglio Direttivo che viene eletto in sede di costituzione;</li>
<li>approvare le modifiche statutarie stabilite dal Consiglio Direttivo;</li>
<li>ratificare l’importo delle quote associative stabilite dal Consiglio Direttivo;</li>
<li>deliberare sullo scioglimento dell&#8217;associazione e sulle modalità di destinazione dell&#8217;eventuale residuo patrimoniale;</li>
<li>indicare i programmi di massima dell&#8217;azione di sviluppo dell&#8217;associazione;</li>
<li>ratificare tutta l&#8217;attività di iniziativa e proposte per il conseguimento degli scopi dell&#8217;associazione operata dal Consiglio Direttivo.</li>
</ol>

<h2 id="articolo_9_il_consiglio_direttivo">Articolo 9 (Il Consiglio Direttivo)</h2>

<p>Il Consiglio Direttivo è composto da un minimo di due ad un massimo di sette membri.
I soci fondatori sono di diritto membri del Consiglio Direttivo; i restanti membri sono eletti dall&#8217;assemblea dei soci.</p>

<p>Il Consiglio Direttivo si riunisce di norma due volte l&#8217;anno od ogni qualvolta il Presidente lo ritenga necessario oppure su richiesta motivata di almeno due terzi dei suoi componenti.
La convocazione è eseguita dal Presidente mediante una comunicazione scritta o via fax o via e-mail almeno dieci giorni prima della riunione.</p>

<p>Esso è validamente costituito con la maggioranza dei suoi componenti.</p>

<p>Le delibere sono adottate con la maggioranza dei presenti.
In caso di parità prevale il voto del Presidente.
Il voto non può essere espresso per rappresentanza o tramite delega.</p>

<p>Il Consiglio Direttivo ha tutti i poteri e le attribuzioni per la gestione dell’istituto che non siano riservati per legge o per statuto all&#8217;assemblea dei soci.
Dura in carica 5 anni.</p>

<p>Le funzioni del Consiglio Direttivo sono:</p>

<ol>
<li>Predisporre il bilancio preventivo e consuntivo da sottoporre all&#8217;approvazione dell&#8217;assemblea dei soci.</li>
<li>Eleggere, tra i suoi componenti, il Presidente.</li>
<li>Stabilire l’importo delle quote associative e la loro periodicità, eventuali contributi annuali o straordinari.</li>
<li>Predisporre ed approvare il regolamento di attuazione del presente statuto (regolamento interno).</li>
<li>Predisporre e stabilire le modifiche del presente statuto da sottoporre all&#8217;assemblea dei soci.</li>
<li>Deliberare sulla decadenza dei soci.</li>
<li>Ratificare le domande di ammissione a socio approvate dal Presidente.</li>
<li>Ratificare le dimissioni accettate dal Presidente.</li>
<li>Deliberare sulla stipulazione di qualsiasi contratto convenzione o altri atti di natura amministrativa o patrimoniali inerenti all&#8217;associazione e/o ratifica l&#8217;attività in merito del Presidente.</li>
<li>Istituire speciali strutture per il migliore funzionamento dell&#8217;associazione.</li>
<li>Ratificare l&#8217;utilizzo dei fondi sociali da parte del presidente.</li>
<li>Svolgere l&#8217;attività di iniziativa e proposte per il conseguimento degli scopi dell&#8217;associazione, demandandone l&#8217;operatività al Presidente.</li>
<li>Ratificare tutte le iniziative assunte autonomamente dal Presidente.</li>
</ol>

<h2 id="articolo_10_il_presidente">Articolo 10 (Il Presidente)</h2>

<p>Il Presidente è eletto tra i componenti del Consiglio Direttivo.</p>

<p>Il Presidente è scelto tra i soci fondatori e solo in presenza di rifiuto di tutti i soci fondatori, potrà essere scelto tra i soci ordinari.
Esso dura in carica fino al termine del mandato, salvo revoca di almeno due terzi del Consiglio Direttivo.</p>

<p>Il Presidente è il legale rappresentante dell’associazione di fronte ai terzi ed in giudizio.
Esegue tutte le delibere del Consiglio Direttivo e dell’assemblea dei soci.
Preside e convoca il lavori dell’assemblea generale e del Consiglio Direttivo, stabilendo l&#8217;ordine del giorno.</p>

<p>Qualora ne ravvisi l&#8217;urgenza o la necessità, il Presidente ha potestà autonoma di iniziativa e il suo operato sarà posto all&#8217;approvazione o ratifica del Consiglio Direttivo.
Può attivare qualsiasi iniziativa per il raggiungimento degli scopi dell&#8217;associazione.</p>

<p>Accerta che si operi in conformità agli interessi dell&#8217;associazione.
Può nominare un Vicepresidente di sua fiducia che, in caso di impedimento o assenza provvisoria del Presidente, assume suoi poteri sempre su delega espressa dello stesso ed in via provvisoria.</p>

<p>Può delegare alcuni soci di stretta fiducia al compimento di determinate attività stabilite dal Presidente stesso per il raggiungimento dei fini dell&#8217;associazione.</p>

<p>Può costituire un suo ufficio per raggiungere i fini dell&#8217;associazione (ufficio di presidenza).
Dispone dei fondi sociali con successiva ratifica del Consiglio Direttivo.</p>

<p>Assegna incarichi esecutivi di settore e le specifiche attività per il raggiungimento dei fini dell&#8217;associazione con comunicazione al Consiglio Direttivo; i soggetti in questione rispondono del loro operato.</p>

<p>Accetta le domande di ammissione a socio.
Accetta le dimissioni da socio.</p>

<h2 id="articolo_11_il_segretario_amministrativo">Articolo 11 (Il Segretario Amministrativo)</h2>

<p>Il Segretario Amministrativo cura gli adempimenti della gestione patrimoniale, dei conti correnti bancari o postali intestati all&#8217;associazione, sottoponendo il tutto alla firma del Presidente.</p>

<p>È nominato direttamente dal Presidente tra uno dei soci fondatori o ordinari in relazione allo stretto rapporto di fiducia che deve intercorrere tra presidente e lo stesso.</p>

<h2 id="articolo_12_scioglimento_dell8217associazione">Articolo 12 (Scioglimento dell&#8217;Associazione)</h2>

<p>L&#8217;assemblea delibera lo scioglimento dell&#8217;associazione quando gli scopi sociali vengono meno ed in ogni caso in cui lo ritenga necessario o opportuno.</p>

<p>La destinazione del patrimonio è stabilita dal regolamento interno, rispettando sempre i principi e gli scopi propri dell&#8217;associazione.</p>

<h2 id="articolo_13_patrimonio_sociale">Articolo 13 (Patrimonio Sociale)</h2>

<p>Il patrimonio dell&#8217;associazione è costituito da:
 1. le quote sociali;
 1. i contributi ed elargizioni dei soci, dello Stato, degli Enti locali, degli Enti pubblici o privati, dei terzi;
 1. le sovvenzioni e i fondi di riserva,
 1. ogni bene mobile od immobile appartenente all&#8217;Associazione (pervenuto per acquisto, donazione, lascito, o altro titolo);
 1. ogni altra entrata che concorra ad incrementare le attività sociali, purché non condizioni i fini dell&#8217;associazione.
Il patrimonio sociale è indivisibile.</p>

<p>È vietato distribuire - anche in modo indiretto - utili e avanzi di gestione, nonché fondi, riserve o capitale durante la vita dell&#8217;associazione, salvo che la destinazione o la distribuzione non siano imposte dalla legge.</p>

<h2 id="articolo_15_rendiconto_economico_e_finanziario">Articolo 15 (Rendiconto Economico e Finanziario)</h2>

<p>Il rendiconto economico e finanziario comprende l&#8217;esercizio sociale dal 1 gennaio al 31 dicembre di ogni anno e deve essere presentato all&#8217;assemblea entro il 31 marzo dell&#8217;anno successivo; il rendiconto dovrà essere approvato entro il 3 maggio di ogni anno.</p>

<h2 id="articolo_16_norma_transitoria">Articolo 16 (Norma Transitoria)</h2>

<p>Fino alla convocazione della prima assemblea generale il consiglio direttivo assume tutte le funzioni dell&#8217;assemblea generale dei soci come specificate nell&#8217;art. 8 del presente statuto.</p>

<p>Il Consiglio Direttivo durante questo periodo potrà cooptare a far parte dello stesso consiglio, nei limiti numerici stabiliti nell&#8217;art. 9 dello statuto, altri soci, su valutazione obiettiva dell’impegno profuso nell&#8217;attività associativa.</p>

<p>Il Consiglio Direttivo e il Presidente rimangono in carica fino alla convocazione dell&#8217;assemblea generale dei soci, per il rinnovo delle cariche sociali.</p>

