<div class="genericblock">Vogliamo che openpolis sia libero e non soggetto a censura, che vengano rispettate le persone, le opinioni e tutte le posizioni politiche. Per questo chiediamo a tutti di rispettare e far rispettare le Regole qui raccolte.<br />
<br />
<h3>1. Pubblicazione diretta</h3>
Per pubblicare e segnalare contenuti nel sito bisogna essere registrati. I contributi  degli utenti vengono pubblicati immediatamente, nel caso risultassero contrari alle regole di pubblicazione verranno successivamente rimossi.<br />
<br />
<h3>2. Identit&agrave; personale</h3>
Non &egrave; consentito attribuirsi l'identit&agrave; di persone diverse da se stessi.<br />
Al momento della pubblicazione devono essere forniti dati personali reali che consentano l'identificazione dell'utente in caso di necessit&agrave;. La scelta di rendere pubblica (nota agli altri utenti) la propria identit&agrave; &egrave; esclusivamente personale e tale scelta pu&ograve; essere modificata dall'utente in qualsiasi momento.<br />
Tuttavia l'uso del nome e cognome reali &egrave; molto consigliato perch&egrave; favorisce la trasparenza e quindi la responsabilit&agrave; e la fiducia reciproche.<br />
<br />
<h3>3. Riservatezza</h3>
E' vietato divulgare informazioni personali che riguardino altri utenti del sito contro la loro volont&agrave;. Non &egrave; consentito pubblicare informazioni che possano permettere di rivelare l'identit&agrave; di minori. <br />
<br />
<h3>4. Comunicazione</h3>
a. Non &egrave; consentito <strong>offendere la dignit&agrave; e la reputazione delle persone</strong>, minacciare gli altri partecipanti al sito, n&eacute; insultare persone, razze, sessi, religioni, condizioni economiche o opinioni politiche; <br />
b. Non si possono pubblicare informazioni di carattere commerciale, promozionale, pubblicitario o di propaganda politica e elettorale, n&eacute; si pu&ograve; trasmettere o diffondere qualsiasi forma di comunicazione o sollecitazione non autorizzata o non richiesta dagli utenti; <br />
c. I contenuti pubblicati dagli utenti (commenti, dichiarazioni, etc.) devono essere pertinenti al contesto nel quale vengono pubblicati. <br />
<br />
<h3>5. Diritto d'autore</h3>
Prima di utilizzare contenuti di qualsiasi tipo (articoli, audio,  video) bisogna accertarsi di non violare i diritti che regolano il loro utilizzo. <br />
<br />
<h3>6. Documentazione e verifica</h3>
I dati e le informazioni inseriti dagli utenti devono essere accuratamente documentati e verificati sia prima della pubblicazione, dagli autori degli inserimenti, sia successivamente, da tutti gli altri utenti. openpolis &egrave; un sistema di documentazione distribuita la cui affidabilit&agrave; dipende dalla qualit&agrave; e dalla affidabilit&agrave; dei contributi degli utenti. Pertanto i dati e le informazioni falsi o privi dei dovuti riscontri verranno rimossi. <br />
<br />
<h3>7. Gestione delle regole</h3>
a. Ci sono tre tipi di utenti registrati in openpolis, cui corrispondono facolt&agrave; differenti: <br />
- utenti (utenti normali e utenti-politici presenti nel sistema): possono inserire dati nella carriera del politico, pubblicare le dichiarazioni del politico, segnalare contenuti con errori o abusi, modificare i contenuti da loro stessi pubblicati; <br />
- moderatori: hanno le stesse facolt&agrave; degli utenti e inoltre possono oscurare i contenuti degli altri utenti (i contenuti non sono pi&ugrave; visibili, vengono messi da parte per essere poi o recuperati o definitivamente eliminati); <br />
- amministratori: hanno le stesse facolt&agrave; dei moderatori e in pi&ugrave; possono eliminare i contenuti oscurati, bloccare gli utenti, creare nuovi moderatori o declassarli.<br />
b. I contenuti e i commenti che violino norme di legge o che siano contrari alle regole di pubblicazione verranno oscurati dai moderatori in modo da non essere pi&ugrave; visibili sul sito e dopo verifica da parte degli amministratori verranno definitivamente cancellati dal sistema. <br />
c. Gli utenti responsabili di abusi o di ripetuti comportamenti contrari alle regole di pubblicazione verranno bloccati dagli amministratori che gli impediranno l'accesso futuro al sito (login). <br />
<br />
<h3>8. Modifiche del regolamento</h3>
Queste regole sono soggette alle verifiche e alle modifiche che si rendessero necessarie. <br />
