<div class="genericblock">
  
  <p>In questa pagina descriviamo quali dati personali degli utenti del sito openpolis e del sito openparlamento ad esso collegato vengono trattati, in che modo e a quali scopi, secondo quanto previsto dall' art. 13 del Codice sulla tutela dei dati personali (Decreto Legislativo 30 giugno 2003 n.196).</p>

  <p><b>Dati forniti dall'utente al momento della registrazione</b></p>
  <p>I dati personali richiesti agli utenti al momento della registrazione (nome, cognome, indirizzo e-mail, comune di residenza) sono quelli strettamente necessari a consentire di gestire l'interazione avanzata e la pubblicazione diretta di contenuti e informazioni da parte degli utenti all'interno del sito. Dato che le informazioni che gli utenti possono aggiungere possono riguardare anche altre persone (i politici italiani) &egrave; necessario richiedere dati personali che permettano di attribuire le aggiunte e le modifiche dei contenuti a delle persone reali. Questo sia per ragioni generali di trasparenza sia per consentire l'identificazione dei responsabili di eventuali abusi.</p>

  <p><b>Dati di navigazione</b></p>
  <p>I sistemi informatici e le procedure software preposte al funzionamento di questo sito web acquisiscono, nel corso del loro normale esercizio, alcuni dati personali la cui trasmissione &egrave; implicita nell'uso dei protocolli di comunicazione di Internet. Si tratta di informazioni che non sono raccolte per essere associate a interessati identificati, ma che per loro stessa natura potrebbero, attraverso elaborazioni ed associazioni con dati detenuti da terzi, permettere di identificare gli utenti. In questa categoria di dati rientrano gli indirizzi IP o i nomi a dominio dei computer utilizzati dagli utenti che si connettono al sito, gli indirizzi in notazione URI (Uniform Resource Identifier) delle risorse richieste, l'orario della richiesta, il metodo utilizzato nel sottoporre la richiesta al server, la dimensione del file ottenuto in risposta, il codice numerico indicante lo stato della risposta data dal server (buon fine, errore, ecc.) ed altri parametri relativi al sistema operativo e all'ambiente informatico dell'utente.
  <br />
  Questi dati possono essere utilizzati al solo fine di ricavare informazioni statistiche anonime sull'uso del sito e per controllarne il corretto funzionamento. I dati potrebbero essere utilizzati per l'accertamento di responsabilit&agrave; in caso di ipotetici reati informatici ai danni del sito: salva questa eventualit&agrave;, allo stato i dati sui contatti web persistono solo per brevi periodi.</p>

  <p><b>Cookies</b></p>
  <p>I siti openpolis e openparlamento sito utilizzano il servizio Google Analytics che fa uso di cookies e delle informazioni relative per tracciare la navigazione degli utenti nelle pagine del sito e fornire elementi di valutazione che ci permettano di migliorare l'efficacia degli strumenti messi a disposizione degli utenti stessi. Google registra queste informazioni sui propri server e le gestisce secondo le regole sulla privacy alle quali si rimanda.</p>
  <p>L'utente pu&ograve; impostare il proprio browser in modo da evitare l'uso dei cookies, ma ci&ograve; pu&ograve; determinare delle limitazioni nell'uso delle funzionalit&agrave; del sito.</p>

  <p><b>Finalit&agrave; del trattamento</b></p>
  <p>Il trattamento dei dati personali &egrave; svolto unicamente per i seguenti scopi:<br />

  - per creare e gestire un data base degli utenti registrati; <br />
  - per fornire agli utenti registrati strumenti per la pubblicazione di contenuti e per l'interazione avanzata;  <br />
  - per la gestione via e-mail di comunicazioni di servizio e di informazioni strettamente necessarie e inerenti alla amministrazione del sito;<br />
  - per l'elaborazione di statistiche anonime;<br />
  - per altri scopi comunque connessi al progetto openpolis e allo svolgimento dell'attivit&agrave; sociale dell'associazione openpolis.<br />
  </p>
  
  <p><b>Modalit&agrave; del trattamento</b></p>
  <p>I dati personali sono trattati con strumenti automatizzati unicamente per le finalit&agrave; su indicate e per il tempo strettamente necessario a conseguire gli scopi per cui sono stati raccolti. Sono state adottate le misure di sicurezza necessarie a prevenire la perdita dei dati, usi illeciti o non corretti ed accessi non autorizzati.</p>

  <p><b>Consenso facoltativo</b></p>
  <p>L'utente &egrave; libero di non autorizzare il trattamento dei propri dati. Il mancato consenso comporta solamente l'impossibilit&agrave; di procedere alla registrazione al sito e quindi all'utilizzo degli strumenti di pubblicazione e di interazione avanzata riservati ai soli utenti registrati. Tuttavia la fruizione in sola lettura di tutti i contenuti del sito &egrave; aperta a chiunque senza necessit&agrave; di registrazione.</p>

  <p><b>Comunicazione dei dati</b></p>
  <p>I dati non saranno comunicati o comunque trasferiti ad altri soggetti. In riferimento ai soli dati riguardanti il nome e cognome, spetta al singolo utente la scelta di renderli pubblici o meno sul sito. Nel caso l'utente dovesse preferire di tenere riservata la propria identit&agrave; questa scelta sar&agrave; rigorosamente rispettata e il nome e cognome verranno resi inaccessibili e non comunicati ad alcuno.</p>

