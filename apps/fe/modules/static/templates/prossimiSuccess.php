<h2>1. Strumenti per la comunit&agrave; (social networking)</h2>
Prevediamo di realizzare strumenti che permettano di intessere e sviluppare relazioni tra gli utenti di openpolis per facilitare la gestione comunitaria del progetto e i suoi sviluppi futuri.<br />
Questi gli strumenti principali:
<ul>
