<li>Indicazione di utenti amici;</li>
<li>Creazione di gruppi (un utente si associa ad altri utenti, gli utenti creano gruppi con la possibilit&agrave; di usare strumenti di collaborazione, etc.);</li>
<li>Integrazione con altre piattaforme di social networking.</li>
</ul>
