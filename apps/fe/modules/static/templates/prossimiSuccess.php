<h2>2. Strumenti per il lobbying comunitario</h2>
Perch&eacute; le attivit&agrave; di <a href="http://it.wikipedia.org/wiki/Lobby">lobbying</a> debbono essere sempre e comunque riservati ai gruppi dominanti?<br />
Perch&eacute; debbono svolgersi sempre nell'ombra, in maniera cos&igrave; poco chiara?</br />
In fondo, il pi&ugrave; delle volte, si tratta di condizionare le decisioni dei politici per spostare risorse a vantaggio di alcuni e, inevitabilmente, a danno di altri. Chi sono gli uni? Chi gli altri?<br />
Con di openpolis vogliamo entrare in questo gioco mettendo a disposizione strumenti che permettano di organizzare forme di azione e pressione, per condizionare l'agenda e le scelte politiche.<br />
Magari potremmo scoprire che l'azione coordinata di molte persone pu&ograve; competere e anche prevalere sugli interessi di pochi, con un confronto alla luce del sole ... <br />
<br /><br />
<h2>3. Nuove informazioni nella pagina del politico </h2>
Nella pagina del politico vanno aggiunte le seguenti sezioni:
<ul>
<li>I procedimenti giudiziari: gli utenti documentano i procedimenti giudiziari che hanno interessato, o interessano, il politico. Qui trattandosi di informazioni particolarmente delicate dovremo mettere a punto un sistema di verifica ad hoc;</li>
<li>Il patrimonio: la <a href="http://www.senato.it/istituzione/108469/108474/genpagspalla.htm">legge n.441 del 5 luglio 1982</a> stabilisce che tutti gli eletti (e non solo) debbano dichiarare tutti i propri beni mobili, immobili, finanziari (titoli azionari, etc.) come pure le spese sostenute in campagna elettorale e gli eventuali finanziatori. Tutte queste informazioni potrebbero essere pubblicate dagli utenti altrimenti resteranno inaccessibili.;</li>
<li>Le spese elettorali: la <a href="http://www.agcom.it/L_naz/L_515_93.htm#14">legge n.10 del 10 dicembre 1993</a> stabilisce che "Le dichiarazioni e i rendiconti depositati dai candidati sono liberamente consultabili presso gli uffici del Collegio". Per renderli veramente accessibili a tutti andrebbero pubblicati su openpolis</li>
</ul>
